/**¿Qué es el control de versiones?*/
/**El control de versiones es un sistema que registra los cambios realizados sobre un archivo o conjunto de archivos a lo largo del tiempo, */
/**de modo que puedas recuperar versiones específicas más adelante.*/
/**A pesar de que los ejemplos de este libro muestran código fuente como archivos bajo control de versiones, en realidad cualquier tipo de archivo */
/**que encuentres en un ordenador puede ponerse bajo control de versiones.*/

/**¿Cuales son las ventajas de utilizar un contr de versiones?*/
/**Acceso al registro de modificaciones sobre un fichero. Esto nos permite además ver los comentarios asociados */
/**a cada modificación y la persona que ha realizado dicha modificación*/
/**Posibilidad de regresar al estado anterior de un fichero o conjunto de ficheros*/
/**Creación de ramas para gestionar cambios que finalmente se mezclarán con la rama principal*/

